# Descripción: Escribe una función llamada area_triangulo que tome la base y la altura
# de un triángulo
# como parámetros y devuelva su área.
def area_triangulo(base,altura):
    area=(base*altura)//2
    return area
print(area_triangulo(10,15))
print(area_triangulo(50,80))
print(area_triangulo(25,38))